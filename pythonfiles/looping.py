#!/bin/python3
#for loop is used when we know a starting and ending point for a given scenario.
#while loop is used when we are not perfect with the ending point.
#For Loop-start to finish of a "iterate".
cars = ["BMW" ,"BenZ" ,"Audi" ,"ToYoTo"]
for x in cars:
	print(x)


#While Loop - Executes as long as the statement is true.

i = 1
while i < 10:#Loop stops when i=10.
	print(i)
	i+=1#addding 1 to something 
