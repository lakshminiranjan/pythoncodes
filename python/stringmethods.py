name = "Nizy"

print(len(name))#prints the length of variable
print(name.find("i"))#gives the index of the element
print(name.capitalize())#makes the name capatilize only first letter of string
print(name.upper())#converts the string into uppercase
print(name.lower())#converts the string into lowercase
print(name.isdigit())#prints true if string is a digit
print(name.isalpha())#prints true if string has a word not a sentence
print(name.count("i"))#counts no.of "i" in string
print(name.replace("N", "n"))#replace old with new
print(name*3)#prints or displays string 3 times